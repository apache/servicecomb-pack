package io.servicecomb.saga.transports.resttemplate;

import org.springframework.cloud.client.loadbalancer.LoadBalanced;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;
import org.springframework.web.client.RestTemplate;

import io.servicecomb.saga.transports.RestTransport;

@Configuration
public class RestTemplateConfig {

  @LoadBalanced
  @Bean
  RestTemplate restTemplate() {
    return new RestTemplate();
  }

  @Bean
  RestTransport restTransport(RestTemplate restTemplate) {
    return new RestTemplateTransport(restTemplate, "http://");
  }
}
